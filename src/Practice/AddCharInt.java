package Practice;

public class AddCharInt {
    public static void main(String[] args) {
        char ch = 'A';  // ASCII value of 'A' = 65
        int num = 5;
        int result = ch + num;

        System.out.println("Character: " + ch);
        System.out.println("Integer: " + num);
        System.out.println("Result (char + int): " + result);
    }
}
