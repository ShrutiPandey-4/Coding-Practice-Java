import java.util.Scanner;
public class add {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a+b;
        int mul = a*b;
        int sub = a-b;
        int div = a/b;
        System.out.println("Sum"+sum);
        System.out.println("Sum"+mul);
        System.out.println("Sum"+sub);
        System.out.println("Sum"+div);
    }
}
